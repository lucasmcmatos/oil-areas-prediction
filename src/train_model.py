"""Train and evaluate the oil probability RandomForest classifier.

Trains on the synthetic dataset generated by generate_dataset.py, using
only sensor-observable features (methane, pressure, position). The
`distance_to_nearest_field_km` and `true_probability` columns are kept in
the dataset for evaluation/calibration purposes only and must never be
used as training features, since they would leak the answer.
"""

from __future__ import annotations

import json
from pathlib import Path

import joblib
import pandas as pd
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import classification_report, mean_absolute_error, roc_auc_score
from sklearn.model_selection import train_test_split

FEATURE_COLUMNS = ["methane_ppm", "pressure_hpa", "latitude", "longitude"]
TARGET_COLUMN = "label"

RANDOM_STATE = 42
TEST_SIZE = 0.2

PROJECT_ROOT = Path(__file__).resolve().parent.parent
DATA_PATH = PROJECT_ROOT / "data" / "synthetic_dataset.csv"
MODEL_PATH = PROJECT_ROOT / "models" / "oil_probability_model.joblib"
METRICS_PATH = PROJECT_ROOT / "models" / "metrics.json"


def build_model() -> RandomForestClassifier:
    return RandomForestClassifier(
        n_estimators=300,
        max_depth=8,
        min_samples_leaf=5,
        class_weight="balanced",
        random_state=RANDOM_STATE,
        n_jobs=-1,
    )


def main() -> None:
    df = pd.read_csv(DATA_PATH)

    X = df[FEATURE_COLUMNS]
    y = df[TARGET_COLUMN]

    X_train, X_test, y_train, y_test, idx_train, idx_test = train_test_split(
        X, y, df.index, test_size=TEST_SIZE, random_state=RANDOM_STATE, stratify=y
    )

    model = build_model()
    model.fit(X_train, y_train)

    predicted_probability = model.predict_proba(X_test)[:, 1]
    predicted_label = model.predict(X_test)

    roc_auc = roc_auc_score(y_test, predicted_probability)
    true_probability_test = df.loc[idx_test, "true_probability"]
    calibration_mae = mean_absolute_error(true_probability_test, predicted_probability)
    report = classification_report(y_test, predicted_label, output_dict=True)
    feature_importances = dict(zip(FEATURE_COLUMNS, model.feature_importances_.tolist()))

    metrics = {
        "roc_auc": roc_auc,
        "calibration_mae_vs_true_probability": calibration_mae,
        "classification_report": report,
        "feature_importances": feature_importances,
        "n_train": len(X_train),
        "n_test": len(X_test),
        "positive_prevalence_train": float(y_train.mean()),
        "positive_prevalence_test": float(y_test.mean()),
    }

    MODEL_PATH.parent.mkdir(parents=True, exist_ok=True)
    joblib.dump(model, MODEL_PATH)
    METRICS_PATH.write_text(json.dumps(metrics, indent=2))

    print(f"ROC-AUC: {roc_auc:.4f}")
    print(f"MAE vs true_probability: {calibration_mae:.4f}")
    print("Feature importances:")
    for name, importance in sorted(feature_importances.items(), key=lambda kv: -kv[1]):
        print(f"  {name}: {importance:.4f}")
    print(f"Model saved -> {MODEL_PATH}")
    print(f"Metrics saved -> {METRICS_PATH}")


if __name__ == "__main__":
    main()
